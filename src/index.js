const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0"
};

const match = {
  "00": "",
  "10": ".",
  "11": "-"
};

function decode(expr) {
  return expr
    .match(/.{10}/g)
    .map(it10 =>
      it10 === "**********"
        ? " "
        : MORSE_TABLE[
            it10
              .match(/../g)
              .map(it2 => match[it2])
              .join("")
          ]
    )
    .join("");
}

module.exports = {
  decode
};
